#include<iostream>
using namespace std;
int main(){
    cout<<(3<5)<<endl;
    cout<<(3>5)<<endl;
    cout<<(3==5)<<endl;
    cout<<(3 != 5)<<endl;

    // logical operator 
cout<<"Logical operator"<<endl;
    cout<<((3>2)&&(9<10))<<endl;
    cout<<((3>6)&&(9<10))<<endl;
    cout<<((3!=6)&&(9<10))<<endl;

    cout<<((3>8)||(9<10))<<endl;







    return 0;

}