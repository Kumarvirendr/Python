# <, >, <=, >= ,==, !=, 


num1=int(input("value 1 is:"))
num2=int(input("value 2 is:"))

print("num1<num2:",num1<num2)
print("num1>num2:",num1>num2)
print("num1<=num2:",num1<=num2)
print("num1>=num2:",num1>=num2)
print("num1==num2:",num1==num2)
print("num1 != num2:",num1!=num2)



